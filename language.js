// Current language state
let currentLanguage = localStorage.getItem('manifestLanguage') || 'kk';

/**
 * Get translation for a key
 */
function t(key) {
  return TRANSLATIONS[currentLanguage][key] || key;
}

/**
 * Switch language
 */
/**
 * Switch language
 */
function switchLanguage(lang) {
  if (!['kk', 'ru'].includes(lang)) return;
  
  currentLanguage = lang;
  localStorage.setItem('manifestLanguage', lang);
  
  // ✅ ADD THIS: Update manifesto HTML
  const manifestoContainer = document.querySelector('.manifesto-container');
  if (manifestoContainer) {
    manifestoContainer.innerHTML = t('manifestoText');
  }
  
  // Update all other translated elements
  updateAllTranslations();
  
  // Update language button active state
  updateLanguageButtons();
  
  console.log(`Language switched to: ${lang}`);
}
/**
 * Update all translated text on the page
 */
function updateAllTranslations() {
  // Title
  document.title = t('pageTitle');
  
  const mainTitle = document.querySelector('h1');
  if (mainTitle) mainTitle.textContent = t('mainTitle');
  
  const subtitle = document.querySelector('p.subtitle');
  if (subtitle) subtitle.textContent = t('subtitle');
  
  // Document label
  const docLabel = document.querySelector('.doc-label');
  if (docLabel) docLabel.textContent = t('documentLabel');
  
  // Consent
  const consentLabel = document.querySelector('label[for="consent-checkbox"]');
  if (consentLabel) consentLabel.textContent = t('consentText');
  
  // Sign button
  const signBtn = document.getElementById('btn-sign');
  if (signBtn) {
    const span = signBtn.querySelector('span');
    if (span) span.textContent = t('signButton');
  }
  
  // Status
  const statusHeader = document.querySelector('.status-header span:first-child');
  if (statusHeader) statusHeader.textContent = t('statusLabel');
  
  // Download section titles (if you have them)
  const downloadCards = document.querySelectorAll('.download-card');
  if (downloadCards.length > 0) {
    const kzCard = downloadCards[0];
    const ruCard = downloadCards[1];
    
    if (kzCard) {
      const title = kzCard.querySelector('.download-title');
      const desc = kzCard.querySelector('.download-desc');
      const btn = kzCard.querySelector('.download-btn');
      if (title) title.textContent = t('downloadKZTitle');
      if (desc) desc.textContent = t('downloadKZDesc');
      if (btn) btn.textContent = t('downloadKZBtn');
    }
    
    if (ruCard) {
      const title = ruCard.querySelector('.download-title');
      const desc = ruCard.querySelector('.download-desc');
      const btn = ruCard.querySelector('.download-btn');
      if (title) title.textContent = t('downloadRUTitle');
      if (desc) desc.textContent = t('downloadRUDesc');
      if (btn) btn.textContent = t('downloadRUBtn');
    }
  }
  
  // Signature counter
  const counterHeader = document.querySelector('.counter-header span:first-child');
  if (counterHeader) counterHeader.textContent = t('signatureCounterLabel');
  
  const counterUnit = document.querySelector('.signature-counter-card span:last-of-type');
  if (counterUnit) counterUnit.textContent = t('signatureCounterUnit');
  
  // Footer
  const footer = document.querySelector('.footer-info');
  if (footer) footer.innerHTML = t('footerInfo');
}

/**
 * Update language button active states
 */
function updateLanguageButtons() {
  const kkBtn = document.getElementById('lang-kk');
  const ruBtn = document.getElementById('lang-ru');
  
  if (kkBtn) {
    kkBtn.style.opacity = currentLanguage === 'kk' ? '1' : '0.5';
    kkBtn.style.borderColor = currentLanguage === 'kk' ? 'rgba(99, 102, 241, 0.5)' : 'rgba(255, 255, 255, 0.1)';
  }
  
  if (ruBtn) {
    ruBtn.style.opacity = currentLanguage === 'ru' ? '1' : '0.5';
    ruBtn.style.borderColor = currentLanguage === 'ru' ? 'rgba(99, 102, 241, 0.5)' : 'rgba(255, 255, 255, 0.1)';
  }
}

/**
 * Initialize language system on page load
 */
/**
 * Initialize language system on page load
 */
function initLanguageSystem() {
  // Update manifesto on load
  const manifestoContainer = document.querySelector('.manifesto-container');
  if (manifestoContainer) {
    manifestoContainer.innerHTML = t('manifestoText');
  }
  
  updateAllTranslations();
  updateLanguageButtons();
}

// Initialize when DOM is ready
document.addEventListener('DOMContentLoaded', initLanguageSystem);

// Expose to global scope
window.switchLanguage = switchLanguage;
window.currentLanguage = () => currentLanguage;
window.t = t;